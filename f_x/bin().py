# bin()函数用于将数字转换为二进制
# 进制转换表：
#
# ↓	             2进制	              8进制	             10进制	            16进制
# 2进制	         -	           bin(int(x, 8))	    bin(int(x, 10))	    bin(int(x, 16))
# 8进制	    oct(int(x, 2))	          -	            oct(int(x, 10))	    oct(int(x, 16))
# 10进制	    str(int(x, 2))	    str(int(x, 8))	         -	            str(int(x, 16))
# 16进制	    hex(int(x, 2))	    hex(int(x, 8))	   hex(int(x, 10))	         -